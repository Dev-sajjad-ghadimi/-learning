#امروز در مورد لیست هاست

#اولی append هستش

a = ["ali" , "mohmad" , "heidar"]
#اول اسم لیست رو صدا میزنی بعد نقطه میزاری و بعد تابع رو مینویسی 
a.append("asghae")
#کار append اینه یه استرینگی بهش میدی و ارخ لیستت ااضافه میکنه 
print(a)
#خروجی : ['ali', 'mohmad', 'heidar', 'asghae']


#بعدی extend هستش 
#برای این که بخایی یه لیست رو به لیست اضافه کنی از extend استفاده میکنی

r = ["sial" , "khiar" , "kian"]

r.extend(["hpsein" , "haan" , "mohamad"])
print(r)
#خروجی: ['sial', 'khiar', 'kian', 'hpsein', 'haan', 'mohamad']
#اگه از append برای اضافع کردن لیست به لیست استفاده کنی خروجی اینجوری میشه 
#خروجی با append: ['sial', 'khiar', 'kian', ['hpsein', 'haan', 'mohamad']]
#لیست میره داخل لیست و اضافه نمیشه


#بعدی insert 

#با insret هر کلمه ای  رو دلت بخواد میتونی هر جا بزاری
k = ["ali" , "hosein"]
k.insert(1 , "hassan")
#اول بهش گفتم کجکا بزاره بعد اون کلمه رو بهش دادم 
print(k)
#خروجی : ['ali', 'hassan', 'hosein']




#بعدی remove

#remove برای پاک کردنه

abc = ["a" , "b" , "d" , "b"]
abc.remove("b")
#در لیست اگه دو تا مث همدیگه باشن اولی رو پاک میکنه و کاری به بقیه کلمات تکراری  نداره
print(abc) 
#خروجی : ['a', 'd', 'b']
#b اول رو پاک کرد
#ولی b دمم رو پاک نکرد



#بعدی  pop هست
#کار pop اینه بهش میگی کدوم استرینگ هارو پاک کنه وبرات پاکشون میکنه
p = ["gh" , "di" , "mi"]
p.pop(0)
print(p)
#خروجی: ['di', 'mi']
#و اگر هم pop رو خالی بدی اخرین کلمه رو پاک میککنه 

l = ["j" , "p"]
l.pop()
print(l)
#خروجی : ['j'] 





#بعدیclear هست که کل لیست پاک میکنه
m = ["m" , "l" , "h" , "n" , "r" , "p"]
m.clear()
print(m) 
#خروجی : []


