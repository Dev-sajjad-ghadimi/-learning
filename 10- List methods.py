#بعدی count 
#کارش اینه که بهت میگه توی لیستت یه استرینگ چند بار  چرخیده یا چند تا وجود داره

uu = ["g" , "i" , "u" , "g" , " g"]

print(uu.count("g"))
print(uu)
#خروجی : 2


#بعدی sort
#کارش اینه استرینگ های داخل لیست رو به حورف a-zمیچینه

z = ["b" , "c" , "a" , "d"]
z.sort()
print(z)
#خروجی: ['a', 'b', 'c', 'd']



#بعدی reverse
#کارش اینه برعکس میکنه اینبار از z-a

n = ['a', 'b', 'c', 'd']
n.reverse()
print(n)
#خروجی : ['d', 'c', 'b', 'a']



#بعدی del
#del هم مال پاک کردنه

